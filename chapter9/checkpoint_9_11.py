# main function to start the program
def main():

	# Write code using the in operator that determines whether 'd' is in mystring.
	# initialize a string variable
	mystring = 'Roses are red, Violets are blue, Honey is sweet and so are you'

	# check if d is in the string using 'in' operator
	if 'd' in mystring:
		print("Yes, 'd' is found in the string.")
	else:
		print("There is no 'd' found in the string.")


# call the main function
main()
